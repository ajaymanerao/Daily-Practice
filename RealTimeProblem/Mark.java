   public static void countMark(int arr[][], int answer[]) {

        for (int i = 0; i < arr.length; i++) {   
            int count = 0;

            for (int j = 0; j < arr[i].length; j++) {  
                if (arr[i][j] == answer[j]) {
                    count++;
                }
            }

            System.out.println("Student " + (i + 1) + "  Score =  " + count);
        }
    }




